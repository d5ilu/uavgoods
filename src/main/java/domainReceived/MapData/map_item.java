package domainReceived.MapData;

public class map_item {
 int x;
 int y;
 int z;

    public map_item(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}

