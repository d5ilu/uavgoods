package domainReceived.MapData;

public class building_item {
    int x;
    int y;
    int l;
    int w;
    int h;

    public building_item(int x, int y, int l, int w, int h) {
        this.x = x;
        this.y = y;
        this.l = l;
        this.w = w;
        this.h = h;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getL() {
        return l;
    }

    public int getW() {
        return w;
    }

    public int getH() {
        return h;
    }
}

